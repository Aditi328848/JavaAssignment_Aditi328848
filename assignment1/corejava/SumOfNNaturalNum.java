package assignment1.corejava;

public class SumOfNNaturalNum {


	public static void main(String[] args) {

		//Natural numbers are all +ve numbers starting with 1 
		int sum=0;
		System.out.println("Sum of 20 Natural numbers are.....");
		for (int i = 1; i <=20; i++) {
			sum=sum+i;
		}
		System.out.println(sum);

	}

}

